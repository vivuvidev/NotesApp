/**
 * Application theme wrapper.
 *
 * **Production**: [MainActivity] always passes [darkTheme] explicitly from the
 * DataStore-backed [MainApplication.isDarkModeFlow] — the single source of truth.
 *
 * **Previews**: [darkTheme] defaults to [isSystemInDarkTheme] so that @Preview
 * composables adapt to the IDE's colour scheme without manual plumbing.
 *
 * All window-level side effects (bar icon tints, scrim, window background) are
 * owned exclusively by [MainActivity] so there is only one place to update them.
 */
@Composable
fun NotesAppTheme(
    darkTheme: Boolean = isSystemInDarkTheme(),
    content: @Composable () -> Unit,
) {
    MaterialTheme(
        colorScheme = if (darkTheme) DarkColorScheme else LightColorScheme,
        typography = AppTypography,
        content = content,
    )
}
